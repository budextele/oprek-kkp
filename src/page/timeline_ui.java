    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (dateStart.getDate() == null || dateEnd.getDate() == null || textPekerjaan.getText().equals("")
                || textPic.getText().equals("") || comboNamaProyek.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Tanggal tidak boleh kosong");
        } else {
            PreparedStatement p;
            try {
                java.sql.Connection con = koneksi.getKoneksi();
                String sql = "INSERT INTO timeline_project (ID, Nama_Project, Tanggal_Mulai, Tanggal_selesai, Pekerjaan) VALUES (?,?,?,?,?)";
                p = (PreparedStatement) con.prepareStatement(sql);
                p.setString(1, textId.getText());
                p.setString(5, textPekerjaan.getText());
                p.setDate(3, new java.sql.Date(dateStart.getDate().getTime()));
                p.setDate(4, new java.sql.Date(dateEnd.getDate().getTime()));
                p.setString(2, comboNamaProyek.getSelectedItem().toString());
                p.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
                loadData();
                loadTable();
            } catch (SQLException e) {
                System.out.println("Error : " + e.getMessage());
            }
        }
    }// GEN-LAST:event_btnSaveActionPerformed

    private void loadData() {
        DefaultComboBoxModel comboBoxNamaProyek = new DefaultComboBoxModel();
        comboBoxNamaProyek.addElement("-- Pilih Nama Proyek --");

        textId.setEditable(false);
        textPic.setEditable(false);

        try {
            java.sql.Connection con = koneksi.getKoneksi();
            Statement stmt = (Statement) con.createStatement();
            // nama proyek
            String sql = "SELECT Nama_Project FROM master_project";
            ResultSet rs = (ResultSet) stmt.executeQuery(sql);
            while (rs.next()) {
                comboBoxNamaProyek.addElement(rs.getString("Nama_Project"));
            }
            comboNamaProyek.setModel(comboBoxNamaProyek);
            con.close();
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }

    }

    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID");
        model.addColumn("Nama Proyek");
        model.addColumn("Tanggal Mulai");
        model.addColumn("Tanggal Selesai");
        model.addColumn("Pekerjaan");

        try {
            java.sql.Connection con = koneksi.getKoneksi();
            Statement stmt = (Statement) con.createStatement();
            String sql = "SELECT * FROM timeline_project";
            ResultSet rs = (ResultSet) stmt.executeQuery(sql);
            while (rs.next()) {
                model.addRow(
                        new Object[] { rs.getString("No"), rs.getString("ID"), rs.getString("Nama_Project"),
                                rs.getString("Tanggal_Mulai"),
                                rs.getString("Tanggal_selesai"), rs.getString("Pekerjaan") });
            }
            tableTimeline.setModel(model);
            con.close();
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }

