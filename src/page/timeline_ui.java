    private void tableTimelineMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tableTimelineMouseClicked
        // TODO add your handling code here:
        int row = tableTimeline.getSelectedRow();
        textId.setText(tableTimeline.getValueAt(row, 1).toString());
        comboNamaProyek.setSelectedItem(tableTimeline.getValueAt(row, 2).toString());
        // dateStart.setDate(Date.valueOf(tableTimeline.getValueAt(row, 2).toString()));
        String ds = tableTimeline.getValueAt(row, 3).toString();
        Date date1;
        String de = tableTimeline.getValueAt(row, 4).toString();
        Date date2;
        try {
            date1 = new SimpleDateFormat("yyyy-MM-dd").parse(ds);
            dateStart.setDate(date1);
            date2 = new SimpleDateFormat("yyyy-MM-dd").parse(de);
            dateEnd.setDate(date2);
        } catch (ParseException ex) {
            Logger.getLogger(timeline_ui.class.getName()).log(Level.SEVERE, null, ex);
        }

        // dateEnd.setDate(Date.valueOf(tableTimeline.getValueAt(row, 3).toString()));
        textPekerjaan.setText(tableTimeline.getValueAt(row, 5).toString());
    }// GEN-LAST:event_tableTimelineMouseClicked

    private void comboNamaProyekItemStateChanged(java.awt.event.ItemEvent evt) {// GEN-FIRST:event_comboNamaProyekItemStateChanged
        // TODO add your handling code here:
        String np = comboNamaProyek.getSelectedItem().toString();

        try {
            java.sql.Connection con = koneksi.getKoneksi();
            Statement stmt = (Statement) con.createStatement();
            String sql = "SELECT PIC, ID FROM master_project WHERE Nama_Project = '" + np + "'";
            ResultSet rs = (ResultSet) stmt.executeQuery(sql);
            while (rs.next()) {
                textPic.setText(rs.getString("PIC"));
                textId.setText(rs.getString("ID"));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }// GEN-LAST:event_comboNamaProyekItemStateChanged

