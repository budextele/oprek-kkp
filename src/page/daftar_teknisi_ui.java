    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama Teknisi");
        model.addColumn("No HP");
        tableTeknisi.setModel(model);
        try {
            java.sql.Connection con = koneksi.getKoneksi();
            Statement stmt = (Statement) con.createStatement();
            String sql = "SELECT * FROM teknisi";
            ResultSet rs = (ResultSet) stmt.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[] { rs.getString("ID"), rs.getString("Nama_Teknisi"),
                        rs.getString("No_Handphone") });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        // String ulv = userSession.getUserLevel();
        System.out.println(ulv);
        // JOptionPane.showMessageDialog(null, "anda login sebgai " + ulv);
        switch (ulv) {
            case "admin":
                btnSave.setEnabled(true);
                btnDell.setEnabled(true);
                btnEdit.setEnabled(true);
                break;
            case "teknisi":
                btnSave.setEnabled(false);
                btnDell.setEnabled(false);
                btnEdit.setEnabled(false);
                break;
            case "ceo":
                btnSave.setEnabled(false);
                btnDell.setEnabled(false);
                btnEdit.setEnabled(false);
                break;
            default:
                break;
        }
    }

