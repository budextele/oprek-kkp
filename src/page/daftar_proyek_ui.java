        private void loadData() {
                DefaultComboBoxModel comboBoxTeknisi = new DefaultComboBoxModel();
                DefaultComboBoxModel comboBoxStatus = new DefaultComboBoxModel();

                comboBoxStatus.addElement("On Progress");
                comboBoxStatus.addElement("Selesai");
                comboStatus.setModel(comboBoxStatus);

                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stmt = (Statement) con.createStatement();
                        String sql = "SELECT Nama_Teknisi FROM teknisi";
                        ResultSet rs = (ResultSet) stmt.executeQuery(sql);
                        while (rs.next()) {
                                comboBoxTeknisi.addElement(rs.getString("Nama_Teknisi"));
                        }
                        comboPic.setModel(comboBoxTeknisi);
                        con.close();
                } catch (SQLException e) {
                        System.out.println("Error : " + e.getMessage());
                }

        }

