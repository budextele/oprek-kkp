        private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEditActionPerformed
                // TODO add your handling code here:
                String id = textId.getText();
                String nama_proyek = textNamaProyek.getText();
                String lokasi = textLokasi.getText();
                String pic = comboPic.getSelectedItem().toString();
                String status = comboStatus.getSelectedItem().toString();
                String sql = "update master_project set Nama_Project='" + nama_proyek + "',Lokasi='" + lokasi
                                + "',PIC='" + pic + "',Status='" + status + "' where ID='" + id + "'";
                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stat = (Statement) con.createStatement();
                        stat.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                        loadTable();
                        con.close();
                } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
        }// GEN-LAST:event_btnEditActionPerformed

