        private void loadData() {
                DefaultComboBoxModel comboBoxTeknisi = new DefaultComboBoxModel();
                DefaultComboBoxModel comboBoxStatus = new DefaultComboBoxModel();

                comboBoxStatus.addElement("On Progress");
                comboBoxStatus.addElement("Selesai");
                comboStatus.setModel(comboBoxStatus);

                textId.setEditable(false);

                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stmt = (Statement) con.createStatement();
                        String sql = "SELECT Nama_Teknisi FROM teknisi";
                        ResultSet rs = (ResultSet) stmt.executeQuery(sql);
                        while (rs.next()) {
                                comboBoxTeknisi.addElement(rs.getString("Nama_Teknisi"));
                        }
                        comboPic.setModel(comboBoxTeknisi);
                        con.close();
                } catch (SQLException e) {
                        System.out.println("Error : " + e.getMessage());
                }
        }

        private void loadTable() {
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("ID");
                model.addColumn("Nama Proyek");
                model.addColumn("Lokasi");
                model.addColumn("PIC");
                model.addColumn("Status");

                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stmt = (Statement) con.createStatement();
                        String sql = "SELECT * FROM master_project";
                        ResultSet rs = (ResultSet) stmt.executeQuery(sql);
                        while (rs.next()) {
                                model.addRow(new Object[] { rs.getString("ID"), rs.getString("Nama_Project"),
                                                rs.getString("Lokasi"), rs.getString("PIC"), rs.getString("Status") });
                        }
                        tableProject.setModel(model);
                        con.close();
                } catch (SQLException e) {
                        System.out.println("Error : " + e.getMessage());
                }

        }

