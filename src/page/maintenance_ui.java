        private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSaveActionPerformed
                // TODO add your handling code here:
                java.sql.Connection con;
                PreparedStatement p;
                String np = comboNamaProyek.getSelectedItem().toString();
                String pic = comboPic.getSelectedItem().toString();
                String action = textAction.getText();
                String problem = textProblem.getText();
                Date mtc = new java.sql.Date(dateMtc.getDate().getTime());

                if (np.equals("--Pilih Nama Proyek--") || pic.equals("--Pilih PIC--") || action.equals("")
                                || problem.equals("")
                                || mtc.equals(null)) {
                        JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
                } else {
                        try {
                                // get date
                                String date = java.time.LocalDate.now().toString();
                                String dt = date.replaceAll("-", "/");
                                System.out.println(dt);

                                // get time
                                String time = java.time.LocalTime.now().toString();
                                LocalTime tm = LocalTime.parse(time);
                                int sc = tm.getSecond();

                                // get 4 first string from namaProyek
                                String noTiket;
                                if (np.length() > 4) {
                                        String np1 = np.substring(0, 4);
                                        System.out.println(np1);
                                        noTiket = "MTC/" + dt + "/" + np1 + "/" + sc;
                                        System.out.println(noTiket);
                                        textNoTiket.setText(noTiket);
                                } else {
                                        noTiket = "MTC/" + dt + "/" + np + "/" + sc;
                                        System.out.println(noTiket);
                                        textNoTiket.setText(noTiket);
                                }

                                con = koneksi.getKoneksi();
                                p = (PreparedStatement) con
                                                .prepareStatement("insert into maintenance values(?,?,?,?,?,?)");
                                p.setString(1, textNoTiket.getText());
                                p.setString(2, np);
                                p.setString(5, pic);
                                p.setString(6, action);
                                p.setString(4, problem);
                                p.setDate(3, (java.sql.Date) mtc);
                                p.executeUpdate();
                                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                                loadData();
                                loadTable();
                        } catch (Exception ex) {
                                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
                        }
                }
        }// GEN-LAST:event_btnSaveActionPerformed

        private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEditActionPerformed
                // TODO add your handling code here:
                int row = tableMtc.getSelectedRow();
                String problem = tableMtc.getValueAt(row, 3).toString();
                String action = tableMtc.getValueAt(row, 5).toString();
                String tgl = tableMtc.getValueAt(row, 2).toString();
                String pic = tableMtc.getValueAt(row, 4).toString();

                String newproblem = textProblem.getText();
                String newaction = textAction.getText();
                String newtgl = dateMtc.getDate().toString();
                String newpic = comboPic.getSelectedItem().toString();

                Date mtc = new java.sql.Date(dateMtc.getDate().getTime());
                if (row == -1) {
                        JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
                } else if (newproblem.equals(problem) && newaction.equals(action) && newtgl.equals(tgl)
                                && newpic.equals(pic)) {
                        JOptionPane.showMessageDialog(null, "Data tidak berubah");
                } else {
                        try {
                                java.sql.Connection con;
                                PreparedStatement p;
                                con = koneksi.getKoneksi();
                                p = (PreparedStatement) con.prepareStatement(
                                                "update maintenance set Problem=?, Action=?, Tanggal=?, PIC=? where No_Ticket=?");
                                p.setString(1, newproblem);
                                p.setString(2, newaction);
                                p.setDate(3, (java.sql.Date) mtc);
                                p.setString(4, newpic);
                                p.setString(5, textNoTiket.getText());
                                p.executeUpdate();
                                JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                                loadData();
                                loadTable();
                        } catch (Exception ex) {
                                JOptionPane.showMessageDialog(null, "Data gagal diubah");
                        }
                }

        }// GEN-LAST:event_btnEditActionPerformed

