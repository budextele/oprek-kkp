        private void loadData() {
                DefaultComboBoxModel model1 = new DefaultComboBoxModel();
                DefaultComboBoxModel model2 = new DefaultComboBoxModel();

                model1.addElement("--Pilih Nama Proyek--");
                model2.addElement("--Pilih PIC--");

                textNoTiket.setEditable(false);

                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stmt = (Statement) con.createStatement();
                        String sql1 = "SELECT Nama_Teknisi FROM teknisi";
                        String sql2 = "SELECT Nama_Project FROM master_project where Status LIKE '%selesai%'";
                        ResultSet rs = (ResultSet) stmt.executeQuery(sql2);
                        while (rs.next()) {
                                model1.addElement(rs.getString("Nama_Project"));
                        }
                        comboNamaProyek.setModel(model1);
                        rs = (ResultSet) stmt.executeQuery(sql1);
                        while (rs.next()) {
                                model2.addElement(rs.getString("Nama_Teknisi"));
                        }
                        comboPic.setModel(model2);
                        con.close();
                } catch (Exception e) {
                        System.out.println("Error : " + e.getMessage());
                }
        }

        private void loadTable() {
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("No Tiket");
                model.addColumn("Nama Proyek");
                model.addColumn("Tanggal");
                model.addColumn("Problem");
                model.addColumn("PIC");
                model.addColumn("Action");

                tableMtc.setModel(model);
                try {
                        java.sql.Connection con = koneksi.getKoneksi();
                        Statement stmt = (Statement) con.createStatement();
                        String sql = "SELECT * FROM maintenance";
                        ResultSet rs = (ResultSet) stmt.executeQuery(sql);
                        while (rs.next()) {
                                model.addRow(new Object[] {
                                                rs.getString("No_Ticket"),
                                                rs.getString("Nama_Project"),
                                                rs.getString("Tanggal"),
                                                rs.getString("Problem"),
                                                rs.getString("PIC"),
                                                rs.getString("Action")
                                });
                        }
                        con.close();
                } catch (Exception e) {
                        System.out.println("Error : " + e.getMessage());
                }
        
                // String ulv = userSession.getUserLevel();
                System.out.println(ulv);
                // JOptionPane.showMessageDialog(null, "anda login sebgai " + ulv);
                switch (ulv) {
                    case "admin":
                        btnSave.setEnabled(true);
                        btnDell.setEnabled(true);
                        btnEdit.setEnabled(true);
                        break;
                    case "teknisi":
                        btnSave.setEnabled(false);
                        btnDell.setEnabled(false);
                        btnEdit.setEnabled(false);
                        break;
                    case "ceo":
                        btnSave.setEnabled(true);
                        btnDell.setEnabled(true);
                        btnEdit.setEnabled(true);
                        break;
                    default:
                        break;
                }
        }

