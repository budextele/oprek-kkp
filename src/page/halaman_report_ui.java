    private void btnUnduh1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnUnduh1ActionPerformed
        // TODO add your handling code here:
        // daftar proyek
        String date = java.time.LocalDate.now().toString();
        String fileName = "D:\\" + date + " - Daftar Proyek.pdf";

        // create document object
        try {
            Document document = new Document() {
            };
            PdfWriter.getInstance((com.itextpdf.text.Document) document, new FileOutputStream(fileName));
            document.open();
            Paragraph p = new Paragraph("Daftar Proyek");
            p.setAlignment(Element.ALIGN_CENTER);
            document.add(p);
            // ad null paragraf
            document.add(new Paragraph("\n"));

            // pull data from db
            String sql = "SELECT * FROM master_project";

            try {
                java.sql.Connection conn = (Connection) koneksi.getKoneksi();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                // create table
                PdfPTable table = new PdfPTable(res.getMetaData().getColumnCount());
                for (int i = 1; i <= res.getMetaData().getColumnCount(); i++) {
                    System.out.println(res.getMetaData().getColumnName(i));
                    table.addCell(res.getMetaData().getColumnName(i));
                }

                while (res.next()) {
                    for (int i = 1; i <= res.getMetaData().getColumnCount(); i++) {
                        table.addCell(res.getString(i));
                    }
                }
                document.add(table);
                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
            document.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        JOptionPane.showMessageDialog(null, "File berhasil diunduh");

    }// GEN-LAST:event_btnUnduh1ActionPerformed

