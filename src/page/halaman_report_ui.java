    private void loadData() {
        DefaultComboBoxModel model1 = new DefaultComboBoxModel();
        DefaultComboBoxModel model2 = new DefaultComboBoxModel();
        DefaultComboBoxModel model3 = new DefaultComboBoxModel();
        DefaultComboBoxModel model4 = new DefaultComboBoxModel();
        DefaultComboBoxModel model5 = new DefaultComboBoxModel();

        model1.addElement("---Daftar Proyek---");
        model2.addElement("---Timeline---");
        model3.addElement("---Report---");
        model4.addElement("---Maintenance---");
        model5.addElement("---BAST---");

        String sql1 = "select Nama_Project from master_project"; // daftar proyek
        String sql2 = "select Nama_Project from master_project where Status LIKE '%progress%'"; // timeline
        String sql3 = "select Nama_Project from master_project where Status LIKE '%progress%'"; // report
        String sql4 = "select Nama_Project from master_project where Status LIKE '%selesai%'"; // maintenance
        String sql5 = "select Nama_Project from master_project where Status LIKE '%progress%'"; // bast

        try {
            java.sql.Connection con;
            PreparedStatement p;
            con = (Connection) koneksi.getKoneksi();
            p = (PreparedStatement) con.prepareStatement(sql1);
            ResultSet rs = (ResultSet) p.executeQuery();
            while (rs.next()) {
                model1.addElement(rs.getString("Nama_Project"));
            }

            p = (PreparedStatement) con.prepareStatement(sql2);
            rs = (ResultSet) p.executeQuery();
            while (rs.next()) {
                model2.addElement(rs.getString("Nama_Project"));
            }

            p = (PreparedStatement) con.prepareStatement(sql3);
            rs = (ResultSet) p.executeQuery();
            while (rs.next()) {
                model3.addElement(rs.getString("Nama_Project"));
            }

            p = (PreparedStatement) con.prepareStatement(sql4);
            rs = (ResultSet) p.executeQuery();
            while (rs.next()) {
                model4.addElement(rs.getString("Nama_Project"));
            }

            p = (PreparedStatement) con.prepareStatement(sql5);
            rs = (ResultSet) p.executeQuery();
            while (rs.next()) {
                model5.addElement(rs.getString("Nama_Project"));
            }

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        // comboDaftarProyek.setModel(model1);
        comboTimeline.setModel(model2);
        comboReport.setModel(model3);

    }

